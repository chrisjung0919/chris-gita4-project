package circlebouncelecture;
class DefineCircle {
    int xDist;
    int yDist;
    int velX;
    int velY;
    String theColor;

    public void setCircle(int x, int y, int vX, int vY, String aColor) {
        xDist = x;
        yDist = y;
        velX = vX;
        velY = vY;
        theColor = aColor;
    }
}
